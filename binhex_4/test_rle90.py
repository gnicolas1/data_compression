import pytest
from BinHex4 import rle90

class Test_rle90():

    def test_case_01(self):
        input = [0x1, 0x1]
        output = rle90(input)
        assert output == input

    def test_case_02(self):
        input = [0x1, 0x1, 0x1]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x3]
    
    def test_case_03(self):
        input = [0x1, 0x1, 0x1, 0x2, 0x2]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x3, 0x2, 0x2]

    def test_case_04(self):
        input = [0x1, 0x1, 0x1, 0x2, 0x2, 0x4]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x3, 0x2, 0x2, 0x4]

    def test_case_05(self):
        input = [0x1, 0x1, 0x1, 0x1, 0x2, 0x2, 0x4, 0x4, 0x4]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x4, 0x2, 0x2, 0x4, 0x90, 0x3]

    def test_case_06(self):
        input = [0x1, 0x1, 0x1, 0x1, 0x2, 0x2, 0x4, 0x4, 0x4, 0x5]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x4, 0x2, 0x2, 0x4, 0x90, 0x3, 0x5]

    def test_case_07(self):
        input = [0x1, 0x1, 0x1, 0x1, 0x2, 0x2, 0x4, 0x4, 0x4, 0x5, 0x6]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x4, 0x2, 0x2, 0x4, 0x90, 0x3, 0x5, 0x6]

    def test_case_08(self):
        input = [0x1, 0x90, 0x90]
        output = rle90(input)
        assert output == [0x1, 0x90, 0x00, 0x90, 0x00]

if __name__ == "__main__":
    exit_code = pytest.main()
    print(exit_code)
